# Obtener el promedio de un estudiante a partir de 3 notas

# Inicio: Definir variables de las notas
nota_t1 = 16
nota_t2 = 10
nota_ef = 13

# Proceso: Definir el proceso de c√°lculo del promedio de notas
# T1 (20%) + T2 (35%) + EF (45%)
promedio_nota = (nota_t1 * 0.2 + nota_t2 * 0.35 + nota_ef * 0.45)


# Salida: Mostrar el resultado del promedio
print(f"El promedio de nota es: {promedio_nota}")